        private void barButtonItem3_ItemClick(object sender, DevExpress.XtraBars.ItemClickEventArgs e)
        {
            var msg = "Esta seguro que desea eliminar ";

            if (_focuseddata is DSDatos.T_MarcaRow)
                msg += "esta marca de equipo";
            else
                if (_focuseddata is DSDatos.T_ModeloRow)
                    msg += "este modelo de equipo";
                else
                    if (_focuseddata is DSDatos.T_TipoModeloMantRow)
                        msg += "esta tipo de mantenimiento";
                    else
                        if (_focuseddata is DSDatos.T_TipoModeloAccionRow)
                            msg += "esta acción";

            msg += " ?";
            if (XtraMessageBox.Show(msg, "Eliminar", MessageBoxButtons.YesNo, MessageBoxIcon.Question) == System.Windows.Forms.DialogResult.Yes)
            {
                HelperAdapter.DeleteRow((DataRow)_focuseddata);

                if (_focuseddata is DSDatos.T_MarcaRow)
                    bindingSource1.RemoveCurrent();

                else
                    if (_focuseddata is DSDatos.T_ModeloRow)
                        focusdgrid.DeleteSelectedRows();


                    else
                        if (_focuseddata is DSDatos.T_TipoModeloMantRow)
                            focusdgrid.DeleteSelectedRows();

                        else
                            if (_focuseddata is DSDatos.T_TipoModeloAccionRow)
                                tTipoModeloAccionBindingSource.RemoveCurrent();


            }
        }

